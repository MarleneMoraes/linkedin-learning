#include <cstdio>

int main()
{
    // Integer types
    char character;
    short int short_integer;
    int integer;
    long int long_integer;
    long long int long_long_integer;

    // Unsigned integer types
    unsigned char character;
    unsigned short int short_integer;
    unsigned int integer;
    unsigned long int long_integer;
    unsigned long long int long_long_integer;

    // Specific width integer types
        // Signed
        int8_t; // 8 bit
        int16_t; // 16 bit
        int32_t; // 32 bit
        int64_t; // 64 bit

        // Unsigned
        uint8_t; // 8 bit
        uint16_t; // 16 bit
        uint32_t; // 32 bit
        uint64_t; // 64 bit
    return 0;
}
